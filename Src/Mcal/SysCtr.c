void SysCtr_Init (void)
{
    RCGCGPIO->B0= SYSCTR_RCC_GPIOA;
    RCGCGPIO->B1= SYSCTR_RCC_GPIOB;
    RCGCGPIO->B2= SYSCTR_RCC_GPIOC;
    RCGCGPIO->B3= SYSCTR_RCC_GPIOD;
    RCGCGPIO->B4= SYSCTR_RCC_GPIOE;
    RCGCGPIO->B5= SYSCTR_RCC_GPIOF;

    RCGCTIMER->B0= SYSCTR_RCC_TIMER0;
    RCGCTIMER->B1= SYSCTR_RCC_TIMER1;
    RCGCTIMER->B2= SYSCTR_RCC_TIMER2;
    RCGCTIMER->B3= SYSCTR_RCC_TIMER3;
    RCGCTIMER->B4= SYSCTR_RCC_TIMER4;
    RCGCTIMER->B5= SYSCTR_RCC_TIMER5;
}

    


